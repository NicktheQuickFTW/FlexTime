// Helper function to get alphabetical sorting name
// UCF is sorted by "Central Florida" not "University of Central Florida"
const getAlphabeticalSortName = (school) => {
  if (school.displayName === 'University of Central Florida') {
    return 'Central Florida';
  }
  return school.displayName;
};

// Get schools in alphabetical order (UCF sorted as "Central Florida")
const getSchoolsAlphabetically = () => {
  return Object.values(BIG12_SCHOOLS).sort((a, b) => {
    const nameA = getAlphabeticalSortName(a);
    const nameB = getAlphabeticalSortName(b);
    return nameA.localeCompare(nameB);
  });
};

