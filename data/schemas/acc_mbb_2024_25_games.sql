-- Create tables if they don't exist
CREATE TABLE IF NOT EXISTS conferences (
    id INTEGER PRIMARY KEY,
    name VARCHAR(50) NOT NULL,
    abbreviation VARCHAR(10) NOT NULL
);

CREATE TABLE IF NOT EXISTS teams (
    id INTEGER PRIMARY KEY,
    conference_id INTEGER,
    school_id INTEGER,
    sport_id INTEGER,
    name VARCHAR(100) NOT NULL,
    abbreviation VARCHAR(10),
    FOREIGN KEY (conference_id) REFERENCES conferences(id)
);

CREATE TABLE IF NOT EXISTS games (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    game_date DATE NOT NULL,
    home_team_id INTEGER,
    away_team_id INTEGER,
    home_score INTEGER,
    away_score INTEGER,
    location_type VARCHAR(10) CHECK (location_type IN ('HOME', 'AWAY', 'NEUTRAL')),
    conference_game BOOLEAN DEFAULT FALSE,
    tournament_game BOOLEAN DEFAULT FALSE,
    season VARCHAR(10) DEFAULT '2024-25',
    net_ranking_home INTEGER,
    net_ranking_away INTEGER,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (home_team_id) REFERENCES teams(id),
    FOREIGN KEY (away_team_id) REFERENCES teams(id)
);

